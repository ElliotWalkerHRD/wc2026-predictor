ON CONFLICT DO NOTHING;


-- ============================================================
--  TABLE: predictions
--  One row per user × round × question
-- ============================================================
CREATE TABLE IF NOT EXISTS public.predictions (
  id           UUID DEFAULT uuid_generate_v4() PRIMARY KEY,
  user_id      UUID REFERENCES auth.users(id) ON DELETE CASCADE NOT NULL,
  round        TEXT NOT NULL,               -- 'round1'…'round8'
  question_key TEXT NOT NULL,              -- e.g. '1.1', '2.w.A', 'm1'
  value        TEXT NOT NULL,              -- JSON-encoded value
  updated_at   TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (user_id, round, question_key)
);

CREATE INDEX IF NOT EXISTS idx_predictions_user ON public.predictions(user_id);
CREATE INDEX IF NOT EXISTS idx_predictions_round ON public.predictions(round);
CREATE INDEX IF NOT EXISTS idx_predictions_match ON public.predictions(question_key)
  WHERE question_key LIKE 'm%';


-- ============================================================
--  TABLE: scores
--  One row per user — running round-by-round totals
-- ============================================================
CREATE TABLE IF NOT EXISTS public.scores (
  id               UUID DEFAULT uuid_generate_v4() PRIMARY KEY,
  user_id          UUID REFERENCES auth.users(id) ON DELETE CASCADE UNIQUE NOT NULL,
  round1_points    INTEGER DEFAULT 0,
  round2_points    INTEGER DEFAULT 0,
  round3_points    INTEGER DEFAULT 0,
  round4_points    INTEGER DEFAULT 0,
  round5_points    INTEGER DEFAULT 0,
  round6_points    INTEGER DEFAULT 0,
  round7_points    INTEGER DEFAULT 0,
  round8_points    INTEGER DEFAULT 0,
  total_points     INTEGER DEFAULT 0,
  updated_at       TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_scores_total ON public.scores(total_points DESC);


-- ============================================================
--  TABLE: match_results
--  Final/live scores per match; admin or API-synced
-- ============================================================
CREATE TABLE IF NOT EXISTS public.match_results (
  match_id    INTEGER PRIMARY KEY,           -- matches GROUP_FIXTURES id / KNOCKOUT id
  home_score  INTEGER,
  away_score  INTEGER,
  status      TEXT DEFAULT 'TIMED',          -- TIMED | IN_PLAY | PAUSED | FINISHED
  home_team   TEXT,                          -- for knockout matches
  away_team   TEXT,
  updated_at  TIMESTAMPTZ DEFAULT NOW()
);


