-- ---- profiles ----
-- Everyone can read profiles (for leaderboard display names/avatars)
CREATE POLICY "profiles: public read"
  ON public.profiles FOR SELECT USING (TRUE);

-- Users can update their own profile
CREATE POLICY "profiles: own update"
  ON public.profiles FOR UPDATE USING (auth.uid() = id);

-- Users can insert their own profile (triggered by handle_new_user, but allow direct too)
CREATE POLICY "profiles: own insert"
  ON public.profiles FOR INSERT WITH CHECK (auth.uid() = id);


-- ---- invite_codes ----
-- Anyone can read invite codes to validate at signup
CREATE POLICY "invite_codes: public read"
  ON public.invite_codes FOR SELECT USING (TRUE);

-- Only admins can insert/update invite codes
CREATE POLICY "invite_codes: admin write"
  ON public.invite_codes FOR ALL USING (
    EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = TRUE)
  );


-- ---- predictions ----
-- Users can read all predictions (for match centre - shows everyone's picks)
CREATE POLICY "predictions: public read"
  ON public.predictions FOR SELECT USING (TRUE);

-- Users can only write their own predictions
CREATE POLICY "predictions: own write"
  ON public.predictions FOR INSERT WITH CHECK (auth.uid() = user_id);

CREATE POLICY "predictions: own update"
  ON public.predictions FOR UPDATE USING (auth.uid() = user_id);

CREATE POLICY "predictions: own delete"
  ON public.predictions FOR DELETE USING (auth.uid() = user_id);


-- ---- scores ----
-- Everyone can read scores (for leaderboard)
CREATE POLICY "scores: public read"
  ON public.scores FOR SELECT USING (TRUE);

-- Only admins or service_role can write scores
-- (Scores are updated by the recalculate Edge Function using service_role key)
CREATE POLICY "scores: admin write"
  ON public.scores FOR ALL USING (
    EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = TRUE)
  );


-- ---- match_results ----
-- Everyone can read match results
CREATE POLICY "match_results: public read"
  ON public.match_results FOR SELECT USING (TRUE);

-- Only admins can write match results
CREATE POLICY "match_results: admin write"
  ON public.match_results FOR ALL USING (
    EXISTS (SELECT 1 FROM public.profiles WHERE id = auth.uid() AND is_admin = TRUE)
  );


-- ============================================================
--  HELPER FUNCTION: Set first registered user as admin
--  Call this manually after your own account is created:
--  SELECT set_admin_by_email('your@email.com');
-- ============================================================
CREATE OR REPLACE FUNCTION public.set_admin_by_email(admin_email TEXT)
RETURNS VOID LANGUAGE PLPGSQL SECURITY DEFINER AS $$
DECLARE
  target_id UUID;
BEGIN
  SELECT id INTO target_id FROM auth.users WHERE email = admin_email LIMIT 1;
  IF target_id IS NOT NULL THEN
    UPDATE public.profiles SET is_admin = TRUE WHERE id = target_id;
    RAISE NOTICE 'Admin set for %', admin_email;
  ELSE
    RAISE EXCEPTION 'User not found: %', admin_email;
  END IF;
END;
$$;


